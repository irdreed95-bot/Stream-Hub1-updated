const dict: Record<Lang, TDict> = { en, ar, fr, tr, de, es, pt, ru, hi };

export type TKey = keyof TDict;

interface I18nContextType {
  lang: Lang;
  setLang: (l: Lang) => void;
  t: (key: TKey) => string;
  isRtl: boolean;
}

const I18nContext = createContext<I18nContextType>({
  lang: "en", setLang: () => {},
  t: (k) => en[k], isRtl: false,
});

export function I18nProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<Lang>(() => {
    return (localStorage.getItem("sarad_lang") as Lang) || "en";
  });

  const langInfo = LANGUAGES.find(l => l.code === lang);
  const isRtl = langInfo?.rtl === true;

  useEffect(() => {
    document.documentElement.setAttribute("dir", isRtl ? "rtl" : "ltr");
    document.documentElement.setAttribute("lang", lang);
    localStorage.setItem("sarad_lang", lang);
  }, [lang, isRtl]);

  const setLang = (l: Lang) => setLangState(l);
  const t = (key: TKey): string => {
    const d = dict[lang];
    return (d && d[key]) ? d[key] as string : en[key] as string;
  };

  return (
    <I18nContext.Provider value={{ lang, setLang, t, isRtl }}>
      {children}
    </I18nContext.Provider>
  );
}

export function useI18n() {
  return useContext(I18nContext);
}
